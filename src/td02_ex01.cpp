#include <iostream>

int main()
{
    int number { 10 };
    std::cout << "Additionner "<< number <<" + 1 = " << number + 1 << std::endl;
    std::cout << "Multiplier "<< number <<" * 3 = " << number * 3 << std::endl;
    std::cout << "Diviser "<< number <<" / 2 = " << number / 2 << std::endl;
    return 0;
}